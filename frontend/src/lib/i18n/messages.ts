// Dot-path keys derived from the English dictionary; all locales share schema.
type Leaves<T, P extends string = ""> = {
  [K in keyof T & string]: T[K] extends string
    ? `${P}${K}`
    : T[K] extends object
      ? Leaves<T[K], `${P}${K}.`>
      : never;
}[keyof T & string];

export type TranslationKey = Leaves<typeof MESSAGES.en>;
